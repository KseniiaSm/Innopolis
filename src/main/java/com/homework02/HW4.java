public class HW4 {
    public static void main(String[] args) {
        String[][] test = new String[10][10];
        for (int i = 0; i < test.length; i++) {
            for (int j = 0; j < test.length; j++) {
                test[i][j] = "-";
                System.out.print(test[i][j]);
            }
            System.out.println();


        }

    }
}




